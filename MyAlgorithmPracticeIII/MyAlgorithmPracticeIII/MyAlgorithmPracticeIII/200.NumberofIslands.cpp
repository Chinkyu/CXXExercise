class Solution {
public:

    void DeleteOne(vector<vector<char>>& grid, int i, int j) {
        int l = grid.size();
        int c = grid[0].size();
        queue< pair<int, int>> q;

        q.push(make_pair(i, j));

        while (q.size() != 0) {
            //pair<int, int> p = q.front();
            int in = q.front().first;
            int jn = q.front().second;
            q.pop();

            grid[in][jn] = '0';

            // left
            if (0 <= jn - 1 && grid[in][jn-1] == '1') {
                q.push(make_pair(in, jn - 1));
                grid[in][jn-1] = '0';
            }

            // right
            if (jn + 1 < c && grid[in][jn + 1] == '1') {
                q.push(make_pair(in, jn + 1));
                grid[in][jn + 1] = '0';
            }

            // up
            if (0 <= in - 1 && grid[in-1][jn] == '1') {
                q.push(make_pair(in-1, jn));
                grid[in-1][jn] = '0';
            }

            // down
            if (in + 1 < l && grid[in+1][jn] == '1') {
                q.push(make_pair(in+1, jn));
                grid[in+1][jn] = '0';
            }
        }
    }


    int numIslands(vector<vector<char>>& grid) {
        int l = grid.size();
        int c = grid[0].size();

        int ans = 0;
        for (int i = 0; i < l; ++i) {
            for (int j = 0; j < c; ++j) {
                if (grid[i][j] == '1') {
                    ans++;
                    DeleteOne(grid, i, j);
                }
            }
        }

        return ans;
    }
};

