using namespace std;

class Solution {
public:
	stack<pair<int, int>> st1, st2;

	int getMinSurrounding(vector<vector<int>>& matrix, int x, int y) {
		int xl = matrix.size();
		int yl = matrix[0].size();

		int min = INT_MAX;
		for (int i = -1; i <= 1; ++i) {
			for (int j = -1; j <= 1; ++j) {
				if (i == 0 && j == 0) continue;
				if (i != 0 && j != 0) continue;
				if (x + i < 0 || xl <= x + i) {
					//min = 0; return 0;
					continue;
				}
				if (y + j < 0 || yl <= y + j) {
					//min = 0; return 0;
					continue;
				}
				if (min > matrix[x + i][y + j]) {
					min = matrix[x + i][y + j];
				}
			}
		}

		return min;
	}

	vector<vector<int>> updateMatrix(vector<vector<int>>& matrix) {
		int xl = matrix.size();
		int yl = matrix[0].size();
		int level = 1;
			
		// find 1
		for (int i = 0; i < xl; ++i) {
			for (int j = 0; j < yl; ++j) {
				if (matrix[i][j] == 1) {
					st1.push(make_pair(i, j));
				}
				//cout << matrix[i][j] << " ";
			}
			//cout << endl;
		}

		if (st1.empty()) return matrix;

		do{
			// clear st2
			while(!st2.empty()) st2.pop();

			level++;
			do {
				int x = st1.top().first;
				int y = st1.top().second;

				int min = getMinSurrounding(matrix, x, y);
				if (matrix[x][y] < min + 1) {
					matrix[x][y] = min + 1;
					//st2.push(make_pair(x, y));
				}
				if(matrix[x][y] >= level) st2.push(make_pair(x, y));
				st1.pop();
			} while (!st1.empty());
			st1 = st2;
		} while (!st2.empty());

		return matrix;
	}
};

