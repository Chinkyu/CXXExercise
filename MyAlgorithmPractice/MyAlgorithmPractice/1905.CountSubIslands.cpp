class Solution {
public:
    void gatherIsland(vector<vector<int>>& grid2, int i, int j, vector<vector<int>>& v) {
        int lr = grid2.size();
        int lc = grid2[0].size();

        queue <vector<int>> q;

        q.push({ i, j });
        grid2[i][j] = 2;

        while (q.size() > 0) {
            vector<int> tv = q.front();
            v.push_back(tv);
            i = tv[0];
            j = tv[1];
            grid2[i][j] = 2;
            q.pop();

            // up ni = i - 1;
            if (i - 1 >= 0 && grid2[i - 1][j] == 1) {q.push({ i - 1, j }); grid2[i - 1][j] = 2;}
            // left nj = j - 1;
            if (j - 1 >= 0 && grid2[i][j - 1] == 1) {q.push({ i, j - 1 }); grid2[i][j-1] = 2;}
            // down ni = i + 1;
            if (i + 1 < lr && grid2[i + 1][j] == 1) {q.push({ i + 1, j }); grid2[i+1][j] = 2;}
            // right nj = j + 1;
            if (j + 1 < lc && grid2[i][j + 1] == 1) {q.push({ i, j + 1 }); grid2[i][j+1] = 2;}
        }
    }

    int countSubIslands(vector<vector<int>>& grid1, vector<vector<int>>& grid2) {
        int lr = grid2.size();
        int lc = grid2[0].size();

        vector<vector<vector<int>>> islands;

        for (int i = 0; i < lr; ++i) {
            for (int j = 0; j < lc; ++j) {

                if (grid2[i][j] == 1) {
                    vector<vector<int>> iv;
                    gatherIsland(grid2, i, j, iv);
                    islands.push_back(iv);
                }
            }
        }

        //
        int ans = 0;
        for (auto& it : islands) {
            bool isAll = true;
            for (auto& jt : it) {
                if (grid1[jt[0]][jt[1]] != 1) {
                    isAll = false;
                    break;
                }
            }

            if (isAll == true) ans++;

        }

        return ans;
    }
};

